// Exercise 8: Operator Precedence
// This program shows how Java evaluates a complex expression.

public class OperatorPrecedence {
    public static void main(String[] args) {
        int result = 10 + 5 * 2; // Multiplication is performed before addition.
        System.out.println("Result of 10 + 5 * 2: " + result);
        // Explanation: 5 * 2 = 10, then 10 + 10 = 20.
    }
}