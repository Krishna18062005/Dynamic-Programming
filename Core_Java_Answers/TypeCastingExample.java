// Exercise 7: Type Casting Example
// This program demonstrates converting between different data types.

public class TypeCastingExample {
    public static void main(String[] args) {
        double dValue = 9.78;
        int intValue = (int) dValue;
        System.out.println("Double to int: " + intValue);

        int intNum = 10;
        double doubleNum = (double) intNum;
        System.out.println("Int to double: " + doubleNum);
    }
}